<h1>Contribute to the game</h1>
<BR />

<p>If you want to get involved in this project as a moderator, administrator, or project leader, just send us an email with your skype username (or some other way to contact you) via moderntimesgit _ yahoo.com. We'll get in touch!</p>

<p>Are you a ...</p>

<p><b>Creative mind?</b>
Then go to our <a href="http://moderntimes.userecho.com/" target="_blank">Pitchen-Hole</a> and suggest new monsters, items, map locations, or whatever else comes to mind!</p>

<p><b>Game designer?</b>
Then <a href="http://moderntimes.github.com/ModernTimes/design.html" target="_blank">take a look at the design documents</a> and propose changes and new ideas!</p>

<p><b>Programmer?</b>
Then <a href="https://github.com/ModernTimes/ModernTimes" target="_blank">fork the project on GitHub</a> and contribute to our code base!</p>

