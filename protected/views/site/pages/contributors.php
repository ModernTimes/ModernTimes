<p><b>Writer?</b>
Then send us your witty descriptions and phrases via the <a href="http://moderntimes.userecho.com/" target="_blank">Pitchen-Hole</a>! We will provide a list of phrases that need more punch later on, to make contributing as a writer even easier. You can also pitch ideas for funny quests or interesting non-player characters.</p>

<p><b>Graphic designer?</b>
Then upload your work and send us a link via the <a href="http://moderntimes.userecho.com/" target="_blank">Pitchen-Hole</a>! Your art can be avatar pictures for player and non-player characters, graphics to be integrated into the Google Maps and Google Streetview canvases, user interface elements, or whatever else you want to design.</p>


